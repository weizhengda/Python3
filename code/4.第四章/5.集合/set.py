
## 1. 集合的特性

## 1.1 set是无序的，没有下表索引,不支持列表的所有方法

type({1,2,3,4,5,6})  ## class 'set'


## 1.2 集合是不重复的

{1,1,2,2,3,3} ## {1,2,3}

## 2. 集合的方法 

1 in {1,2,3} ## true

1 not in {1,2,3}  ## false

## 集合的差集
{1,2,3,4,5,6} - {3,4}  ## {1,2,5,6}

## 集合的交集
{1,2,3,4,5,6} & {3,4}  ## {3,4}

## 集合的合集
{1,2,3,4,5,6} | {3,4,7} ## {1,2,3,4,5,6,7}

## 空的集合
type({})  ## class 'dict'
type(set()) ## class 'set'
len(set())  ## 0


